var h = document.querySelector(".breakfast");
h.style.display="none";


document.querySelector(".br").onclick = function(){
    // var f = document.querySelector(".breakfast");
    if (h.style.display === "none") {
              h.style.display = "block";
            } else {
              h.style.display = "none";
            }
};

